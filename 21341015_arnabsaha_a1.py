import heapq

def Manhattan_distance(p1,p2):
  x = p1[0]-p2[0]
  y = p1[1]-p2[1]
  return abs(x+y)

def A_star(maze, start, goal, n, m):
  directions = [(-1, 0, 'U'), (1, 0, 'D'), (0, -1, 'L'), (0, 1, 'R')]

  visited_node=[]
  for i in range (n):
    row=[]
    for j in range (m):
      row.append(False)
    visited_node.append(row)
  queue1=[]
  heapq.heappush(queue1, (Manhattan_distance(start,goal), 0, start, ""))
  while queue1:
        f, g, (x, y), path = heapq.heappop(queue1)

        if (x, y) == goal:
            print(g)
            print(path)
            return

        if visited_node[x][y]:
            continue
        visited_node[x][y] = True

        for dx, dy, move in directions:
            nx, ny = x + dx, y + dy
            if 0 <= nx < n and 0 <= ny < m and maze[nx][ny] == '0' and not visited_node[nx][ny]:
                new_g = g + 1
                new_h = Manhattan_distance((nx, ny), goal)
                heapq.heappush(queue1, (new_g + new_h, new_g, (nx, ny), path + move))

  print(-1)

