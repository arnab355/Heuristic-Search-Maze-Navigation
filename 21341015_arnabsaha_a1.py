from collections import deque

def admissible_check(n, m, start, goal, heuristic_values, edges):
    graph = [[] for _ in range(n + 1)]
    for u, v in edges:
        graph[u].append(v)
        graph[v].append(u)

    distance = [float('inf')] * (n + 1)
    queue = deque()
    queue.append(goal)
    distance[goal] = 0

    while queue:
        node = queue.popleft()
        for neighbor in graph[node]:
            if distance[neighbor] == float('inf'):
                distance[neighbor] = distance[node] + 1
                queue.append(neighbor)

    not_admissible = []
    for node in range(1, n + 1):
        if heuristic_values[node] > distance[node]:
            not_admissible.append(node)

    if not not_admissible:
        print(1)
    else:
        print(0)
        print(*not_admissible)

n, m = map(int, input().split())
start, goal = map(int, input().split())

heuristic_values = [0] * (n + 1)
for _ in range(n):
    node, h = map(int, input().split())
    heuristic_values[node] = h

edges = []
for _ in range(m):
    u, v = map(int, input().split())
    edges.append((u, v))


admissible_check(n, m, start, goal, heuristic_values, edges)